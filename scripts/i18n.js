function normalizeLocale(input = "en") {
  const value = String(input || "en").trim().toLowerCase();
  if (["zh", "zh-cn", "zh-hans", "zh_hans"].includes(value)) return "zh-CN";
  if (["zh-tw", "zh-hant", "zh_hant"].includes(value)) return "zh-TW";
  if (value.startsWith("de")) return "de";
  if (value.startsWith("ja")) return "ja";
  return "en";
}

function resolveLocalizedValue(value, locale = "en") {
  if (!value || typeof value !== "object" || Array.isArray(value)) return value;
  const normalized = normalizeLocale(locale);
  return value[normalized] ?? value.en ?? Object.values(value)[0];
}

function translate(locale, key, vars = {}) {
  const normalized = normalizeLocale(locale);
  const template = UI_TRANSLATIONS[normalized]?.[key] ?? UI_TRANSLATIONS.en[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ""));
}

